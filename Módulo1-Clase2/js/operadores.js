//Operadores de comparación
/*
Se utilizan para comparar dos valores. Como resultado de su ejecución devolver el tipo de dato lógico - true o false, es decir, "sí" o "no".

a > b y a < b - más/menos
a >= b y a <= b - más/menos o igual
a == b - igualdad
a != b - desigualdad
a === b - igualdad estricta
a !== b - desigualdad estricta
*/

const x = 5;
const y = 10;
const z = 5;


console.log("x > y:", x > y);
console.log("x < y:", x < y);
console.log("x < z:", x < z);
console.log("x <= z:", x <= z);
console.log("x === y:", x === y);
console.log("x === z:", x === z);
console.log("x !== y:", x !== y);
console.log("x !== z:", x !== z);

console.log(true === true);
console.log(true === false);
